""" 
Archivo de rutas para poder manejar las peticiones para cerrar sesión
"""

logout = Blueprint('logout',__name__)

@logout.route('/logout')
def salir():
    """
    Método para cerrar sesión

    Returns: mensaje de éxito
    """

    if 'email' in session:
        session.pop('email')
    return jsonify({'msg':'bien'})